    def _execute_trade(self, model_name: str, signal: ModelSignal,
                       adjusted_confidence: Optional[float] = None) -> TradeExecution:
        """Execute a new trade with dynamic weight-adjusted confidence."""
        # Use adjusted confidence for position sizing (falls back to raw)
        effective_confidence = adjusted_confidence or signal.confidence
        
        # Calculate position size using Dynamic Fractional Kelly
        size = self._calculate_position_size(effective_confidence, signal.regime)
        
        # Create trade record
        trade = TradeExecution(
            model_name=model_name,
            signal_type=signal.signal_type,
            entry_price=signal.entry_price,
            quantity=size,
            position_value=size * signal.entry_price,
            status=TradeStatus.OPEN,
            regime=signal.regime,
            confidence=effective_confidence,
        )
        
        # Apply commission
        trade.commission = self._calculate_commission(trade.position_value)
        
        # Apply slippage
        trade.slippage = self._calculate_slippage(signal.entry_price)
        
        # --- Execute Silver Hedge ---
        hedge_size = self.kalman_hedge.get_hedge_size(size, self.current_beta, signal.regime)
        if hedge_size > 0:
            latest_silver = self.silver_feed.latest
            silver_price = latest_silver.xag if latest_silver.is_valid() else 30.0
            
            trade.silver_quantity = hedge_size
            trade.silver_entry_price = silver_price
            
            hedge_value = hedge_size * silver_price
            trade.commission += self._calculate_commission(hedge_value)
            trade.slippage += self._calculate_slippage(silver_price)
            
            if signal.signal_type == SignalType.LONG:
                self.position_sizes["XAGUSD"] = -hedge_size # SHORT hedge
                self.portfolio.current_cash -= hedge_value
            else:
                self.position_sizes["XAGUSD"] = hedge_size # LONG hedge
                self.portfolio.current_cash += hedge_value
                
            logger.info(f"Silver Hedge Executed: {hedge_size:.2f} oz @ ${silver_price:.2f} (β={self.current_beta:.2f})")
        
        # Update portfolio
        if signal.signal_type == SignalType.LONG:
            self.position_sizes["XAUUSD"] = size
            self.portfolio.current_cash -= trade.position_value
        else:  # SHORT
            self.position_sizes["XAUUSD"] = -size
            self.portfolio.current_cash += trade.position_value
        
        # Track trade
        self.trades.append(trade)
        self.daily_trades.append(trade)
        self.current_position = trade
        
        # Log with weight context
        if self.config.use_dynamic_weights:
            weights = self.weight_adjuster.get_weights(signal.regime)
            w = weights.get(model_name, 0)
            logger.info(
                f"Trade executed: {model_name} {trade.signal_type.value} "
                f"{size:.2f} oz @ {signal.entry_price:.2f} | "
                f"regime={signal.regime} weight={w:.1%} adj_conf={effective_confidence:.2f}"
            )
        else:
            logger.info(
                f"Trade executed: {model_name} {trade.signal_type.value} "
                f"{size:.2f} oz @ {signal.entry_price:.2f}"
            )
        
        return trade
    
    def _close_position(self, timestamp: datetime, exit_price: float) -> Optional[TradeExecution]:
        """Close current open position and feed result back to weight adjuster."""
        if not self.current_position or self.current_position.status != TradeStatus.OPEN:
            return None
        
        trade = self.current_position
        trade.exit_price = exit_price
        trade.exit_time = timestamp
        
        # Calculate P&L for Gold
        pnl, pnl_pct = self._calculate_pnl(
            trade.signal_type,
            trade.entry_price,
            exit_price,
            trade.quantity
        )
        trade.pnl = pnl
        
        # Calculate P&L for Silver Hedge
        if trade.silver_quantity > 0:
            latest_silver = self.silver_feed.latest
            silver_exit = latest_silver.xag if latest_silver.is_valid() else 30.0
            trade.silver_exit_price = silver_exit
            
            # Hedge direction is opposite of Gold
            hedge_type = SignalType.SHORT if trade.signal_type == SignalType.LONG else SignalType.LONG
            silver_pnl, _ = self._calculate_pnl(
                hedge_type, 
                trade.silver_entry_price, 
                silver_exit, 
                trade.silver_quantity
            )
            trade.silver_pnl = silver_pnl
            trade.pnl += silver_pnl
            
            # Close Silver leg in position tracker
            self.position_sizes["XAGUSD"] = 0
            
            # Adjust cash for closing silver
            hedge_val = trade.silver_quantity * silver_exit
            if hedge_type == SignalType.LONG:
                self.portfolio.current_cash += hedge_val
            else:
                self.portfolio.current_cash -= hedge_val
                
        # Finalize trade P&L (Gold + Silver - Costs)
        trade.pnl = trade.pnl - trade.commission - trade.slippage
        trade.pnl_pct = (trade.pnl / trade.position_value) * 100 if trade.position_value > 0 else 0
        
        trade.status = TradeStatus.CLOSED
        
        # Update portfolio
        self.position_sizes["XAUUSD"] = 0
        if trade.signal_type == SignalType.LONG:
            self.portfolio.current_cash += trade.quantity * exit_price - trade.commission - trade.slippage
        else:
            self.portfolio.current_cash -= trade.quantity * exit_price + trade.commission + trade.slippage
        self.daily_pnl += trade.pnl
        
        # --- Feed trade result back to Dynamic Weight Adjuster ---
        # This enables performance-adaptive weighting: models that
        # perform well recently get higher future weights.
        if self.config.use_dynamic_weights:
            self.weight_adjuster.record_trade_result(
                model_name=trade.model_name,
                pnl=trade.pnl,
                return_pct=trade.pnl_pct,
            )
        
        logger.info(f"Position closed: P&L ${trade.pnl:.2f} ({trade.pnl_pct:.2f}%)")

        # --- Retroactively update P&L in prediction CSV ---
        try:
            from src.paper_trading.prediction_logger import update_pnl_for_trade
            ts_prefix = trade.entry_time.strftime("%Y-%m-%dT%H:%M") if trade.entry_time else ""
            if ts_prefix:
                update_pnl_for_trade(ts_prefix, trade.pnl)
        except Exception as e:
            logger.debug(f"Could not update prediction CSV P&L: {e}")
        
        self.current_position = None
        return trade
    
    def _calculate_position_size(self, confidence: float, regime: str = "NORMAL") -> float:
        """Calculate position size using Dynamic Fractional Kelly criterion."""
        # Estimate win probability from confidence
        win_prob = (confidence + 1.0) / 2.0  # Scale 0.5-1.0 based on confidence
        
        # Kelly fraction: f = (p * b - q) / b
        # Simplified for gold: assume 1:1 payoff
        kelly_fraction = win_prob - (1 - win_prob)
        
        # Dynamic Kelly Multiplier from RL Execution Agent
        rl_params = get_rl_agent().get_execution_parameters(
            regime=regime,
            volatility=0.02,
            confidence=confidence
        )
        regime_multiplier = rl_params["kelly_multiplier"]
            
        # Apply Kelly fraction, regime multiplier, and config limit
        position_fraction = kelly_fraction * self.config.kelly_fraction * regime_multiplier
        position_fraction = min(position_fraction, self.config.max_position_pct)
        position_fraction = max(position_fraction, 0.01)  # Minimum position
        
        # Convert to ounces
        current_price = self.current_position.entry_price if self.current_position else 2000.0
        position_value = self._create_portfolio_snapshot(current_price).total_value * position_fraction
        position_ounces = position_value / current_price
        
        return position_ounces
    
    def _calculate_commission(self, trade_value: float) -> float:
        """Calculate commission for trade."""
        return max(
            self.config.commission_per_trade,
            trade_value * self.config.commission_pct
        )
    
    def _calculate_slippage(self, price: float) -> float:
        """Calculate slippage for trade."""
        return price * (self.config.slippage_pct / 100.0)
    
    def _calculate_pnl(self, signal_type: SignalType, entry: float, exit: float, quantity: float) -> Tuple[float, float]:
        """Calculate P&L for a trade."""
        if signal_type == SignalType.LONG:
            pnl = (exit - entry) * quantity
        else:  # SHORT
            pnl = (entry - exit) * quantity
        
        pnl_pct = (pnl / (entry * quantity)) * 100 if entry > 0 else 0
        return pnl, pnl_pct
    
    def _check_risk_limits(self) -> bool:
        """Check if trading can continue based on risk limits."""
        total_equity = self._create_portfolio_snapshot().total_value
        
        # Check daily loss limit
        if self.daily_pnl < -total_equity * self.config.max_daily_loss_pct:
            logger.warning(f"Daily loss limit exceeded: ${self.daily_pnl:.2f}")
            return False
        
        # Check maximum drawdown
        portfolio_return = (total_equity - self.config.initial_capital) / self.config.initial_capital
        if portfolio_return < -self.config.max_drawdown_pct:
            logger.warning(f"Maximum drawdown exceeded: {portfolio_return*100:.2f}%")
            return False
        
        return True
    
    def _calculate_win_rate(self) -> float:
        """Calculate win rate from closed trades."""
        if not self.trades:
            return 0.0
        
        closed_trades = [t for t in self.trades if t.status == TradeStatus.CLOSED]
        if not closed_trades:
            return 0.0
        
        wins = len([t for t in closed_trades if t.pnl > 0])
        return wins / len(closed_trades)
    
    def _create_portfolio_snapshot(self, current_price: Optional[float] = None) -> PortfolioSnapshot:
        """Create a point-in-time portfolio snapshot."""
        if current_price is None:
            current_price = self.current_position.entry_price if self.current_position else 2000.0
        
        # Calculate position value
        position_quantity = self.position_sizes.get("XAUUSD", 0)
        position_value = position_quantity * current_price
        
        # Calculate P&L
        pnl_realized = sum(t.pnl for t in self.trades if t.status == TradeStatus.CLOSED)
        pnl_unrealized = sum(t.pnl for t in self.trades if t.status == TradeStatus.OPEN)
        pnl_total = pnl_realized + pnl_unrealized
        
        # Calculate metrics
        total_value = self.portfolio.current_cash + position_value
        return_pct = ((total_value - self.config.initial_capital) / self.config.initial_capital) * 100
        
        return PortfolioSnapshot(
            timestamp=datetime.now(),
            total_value=total_value,
            cash=self.portfolio.current_cash,
            position_quantity=position_quantity,
            position_value=position_value,
            pnl_realized=pnl_realized,
            pnl_unrealized=pnl_unrealized,
            pnl_total=pnl_total,
            daily_pnl=self.daily_pnl,
            return_pct=return_pct,
            sharpe_ratio=self._calculate_sharpe(),
            max_drawdown=self._calculate_max_drawdown(),
            win_rate=self._calculate_win_rate(),
            num_trades=len(self.trades),
        )
    
    def _calculate_sharpe(self, risk_free_rate: float = 0.02) -> float:
        """Calculate Sharpe ratio from portfolio snapshots."""
        if len(self.portfolio_snapshots) < 2:
            return 0.0
        
        returns = []
        for i in range(1, len(self.portfolio_snapshots)):
            prev = self.portfolio_snapshots[i-1]
            curr = self.portfolio_snapshots[i]
            ret = (curr.total_value - prev.total_value) / prev.total_value
            returns.append(ret)
        
        if not returns:
            return 0.0
        
        mean_return = np.mean(returns)
        std_return = np.std(returns)
        
        if std_return == 0:
            return 0.0
        
        return (mean_return - risk_free_rate / 252) / std_return * np.sqrt(252)
    
    def _calculate_max_drawdown(self) -> float:
        """Calculate maximum drawdown from portfolio values."""
        if not self.portfolio_snapshots:
            return 0.0
        
        equity_curve = [s.total_value for s in self.portfolio_snapshots]
        running_max = np.maximum.accumulate(equity_curve)
        drawdown = (np.array(equity_curve) - running_max) / running_max
        
        return float(np.min(drawdown)) * 100
    
    def get_total_pnl(self) -> float:
        """Get total realized P&L."""
        return sum(t.pnl for t in self.trades if t.status == TradeStatus.CLOSED)
    
