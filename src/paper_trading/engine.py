class SignalType(Enum):
    """Trading signal types."""
    LONG = "LONG"
    SHORT = "SHORT"
    CLOSE = "CLOSE"
    HOLD = "HOLD"


class TradeStatus(Enum):
    """Trade status tracking."""
    PENDING = "PENDING"
    OPEN = "OPEN"
    CLOSED = "CLOSED"
    CANCELLED = "CANCELLED"
    ERROR = "ERROR"


@dataclass
class ModelSignal:
    """Signal generated by a model."""
    model_name: str  # 'wavelet', 'hmm', 'lstm', 'tft', 'genetic', 'ensemble'
    signal_type: SignalType
    confidence: float  # 0-1
    entry_price: float
    current_price: float
    timestamp: datetime
    reasoning: str = ""
    regime: str = "NORMAL"  # Market regime


@dataclass
class TradeExecution:
    """Executed trade record."""
    trade_id: str = field(default_factory=lambda: str(uuid.uuid4())[:8])
    timestamp: datetime = field(default_factory=datetime.now)
    model_name: str = ""
    signal_type: SignalType = SignalType.HOLD
    entry_price: float = 0.0
    entry_time: datetime = field(default_factory=datetime.now)
    exit_price: Optional[float] = None
    exit_time: Optional[datetime] = None
    quantity: float = 0.0  # Ounces of gold
    position_value: float = 0.0
    pnl: float = 0.0  # P&L in dollars
    pnl_pct: float = 0.0  # P&L percentage
    commission: float = 0.0
    slippage: float = 0.0
    status: TradeStatus = TradeStatus.PENDING
    regime: str = "NORMAL"
    confidence: float = 0.0
    trailing_stop: Optional[float] = None
    high_water_mark: Optional[float] = None
    
    # Silver Hedge tracking
    silver_quantity: float = 0.0
    silver_entry_price: float = 0.0
    silver_exit_price: Optional[float] = None
    silver_pnl: float = 0.0
    
    def __repr__(self):
        return f"Trade({self.trade_id}) {self.model_name} {self.signal_type.value} @ {self.entry_price:.2f}"


@dataclass
class PortfolioSnapshot:
    """Point-in-time portfolio state."""
    timestamp: datetime
    total_value: float
    cash: float
    position_quantity: float  # Gold quantity in ounces
    position_value: float  # Mark-to-market value
    pnl_realized: float  # Closed trades P&L
    pnl_unrealized: float  # Open position P&L
    pnl_total: float  # Total P&L
    daily_pnl: float
    return_pct: float  # Return as percentage
    sharpe_ratio: float = 0.0
    max_drawdown: float = 0.0
    win_rate: float = 0.0
    num_trades: int = 0


@dataclass
class PaperTradingConfig:
    """Configuration for paper trading."""
    initial_capital: float = 100000.0      # Starting capital ($)
    symbol: str = "XAUUSD"                 # Trading instrument
    
    # Position sizing
    kelly_fraction: float = 0.25           # Kelly criterion fraction
    max_position_pct: float = 0.10         # Max position as % of capital
    max_daily_loss_pct: float = 0.02       # Max daily loss % (circuit breaker)
    max_drawdown_pct: float = 0.15         # Max drawdown % (stop all trading)
    
    # Execution
    commission_per_trade: float = 5.0
    commission_pct: float = 0.0001
    slippage_model: str = "spread"         # 'fixed', 'spread', 'impact'
    slippage_pct: float = 0.01             # Typical bid-ask spread for gold (1 bps)
    
    # Signal configuration
    min_confidence: float = 0.75            # Min confidence to trade
    min_holding_bars: int = 8               # Min holding period (bars) before exiting/flipping position
    # Dynamic weights: regime-conditional base weights (adapted at runtime)
    # These are starting weights per regime; the DynamicWeightAdjuster
    # further adapts them based on each model's rolling Sharpe ratio.
    signal_weights: Dict[str, float] = field(default_factory=lambda: {
        "wavelet": 0.30,   # Denoising — best in noisy/normal markets
        "hmm":     0.30,   # Regime detection — critical during transitions
        "lstm":    0.05,   # Reduced complexity
        "tft":     0.05,   # Reduced complexity
        "genetic": 0.0,    # Disabled
        "nlp":     0.0,    # Disabled
        "ensemble": 0.30,  # Meta-learner
    })
    use_dynamic_weights: bool = True       # Enable regime-adaptive weighting
    
    # Trading hours
    trading_enabled: bool = True
    trading_start_hour: int = 0            # 00:00 UTC
    trading_end_hour: int = 23             # 23:59 UTC


class PaperTradingEngine:
    """
    Core paper trading engine.
    
    Orchestrates:
    1. Signal generation from all 6 models
    2. Position sizing and risk checks
    3. Order execution simulation
    4. P&L tracking and monitoring
    5. Risk management enforcement
    """
    
    def __init__(self, config: Optional[PaperTradingConfig] = None):
        """Initialize paper trading engine."""
        self.config = config or PaperTradingConfig()
        self.state_config = get_config()
        self._lock = threading.Lock()
        
        # Core state
        self.status = "INITIALIZED"
        self.started_at: Optional[datetime] = None
        self.stopped_at: Optional[datetime] = None
        
        # Portfolio tracking
        self.portfolio = PortfolioTracker(self.config.initial_capital)
        # Simple position size tracking (separate from PortfolioTracker's open_positions)
        self.position_sizes: Dict[str, float] = {}
        self.execution_sim = ExecutionSimulator(ExecutionConfig(
            commission_per_trade=self.config.commission_per_trade,
            commission_pct=self.config.commission_pct,
            slippage_model=SlippageModel(self.config.slippage_model),
            slippage_pct=self.config.slippage_pct,
        ))
        
        # Trade history
        self.trades: List[TradeExecution] = []
        self.current_position: Optional[TradeExecution] = None
        self.portfolio_snapshots: List[PortfolioSnapshot] = []
        
        # Daily tracking
        self.day_started_at: Optional[datetime] = None
        self.daily_pnl: float = 0.0
        self.daily_trades: List[TradeExecution] = []
        
        # Model signals
        self.last_signals: Dict[str, ModelSignal] = {}
        self.signal_history: Dict[str, List[ModelSignal]] = {
            model: [] for model in ["wavelet", "hmm", "lstm", "tft", "genetic", "nlp", "ensemble"]
        }
        
        # Dynamic weight adjuster (real-world regime-adaptive weighting)
        self.weight_adjuster: DynamicWeightAdjuster = get_weight_adjuster()
        
        # Monitoring (Phase 6)
        self.health_monitor: Optional[HealthMonitor] = None
        self.performance_monitor: Optional[ModelPerformanceMonitor] = None
        
        # Silver Feed and Kalman Hedge
        self.silver_feed = SilverFeedAdapter(
            goldapi_key=os.getenv("GOLDAPI_KEY", ""),
            metalpriceapi_key=os.getenv("METALPRICEAPI_KEY", ""),
            fetch_interval_s=30
        )
        self.kalman_hedge = KalmanHedgeEngine()
        self.current_beta = 1.0
        
        logger.info(
            f"Paper Trading Engine initialized with ${self.config.initial_capital:,.0f} capital | "
            f"Dynamic weights: {'ON' if self.config.use_dynamic_weights else 'OFF'}"
        )
    
    def start(self) -> Dict:
        """Start paper trading."""
        if self.status == "RUNNING":
            logger.warning("Paper trading already running")
            return {"status": "RUNNING", "message": "Already started"}
        
        self.status = "RUNNING"
        self.started_at = datetime.now()
        self.day_started_at = datetime.now()
        self.daily_pnl = 0.0
        
        self.silver_feed.start()
        
        logger.info("Paper trading started")
        return {
            "status": "RUNNING",
            "started_at": self.started_at.isoformat(),
            "initial_capital": self.config.initial_capital,
            "current_value": self._create_portfolio_snapshot().total_value,
        }
    
    def stop(self) -> Dict:
        """Stop paper trading and close all positions."""
        if self.status != "RUNNING":
            return {"status": self.status, "message": "Not running"}
        
        # Close current position if open
        if self.current_position and self.current_position.status == TradeStatus.OPEN:
            exit_price = self.current_position.entry_price
            for sig in self.last_signals.values():
                if sig.current_price > 0:
                    exit_price = sig.current_price
                    break
            self._close_position(datetime.now(), exit_price)
        
        self.status = "STOPPED"
        self.stopped_at = datetime.now()
        
        self.silver_feed.stop()
        
        logger.info(f"Paper trading stopped. Final P&L: ${self.get_total_pnl():.2f}")
        return {
            "status": "STOPPED",
            "stopped_at": self.stopped_at.isoformat(),
            "final_value": self._create_portfolio_snapshot().total_value,
            "total_pnl": self.get_total_pnl(),
            "total_trades": len(self.trades),
        }
    
    def get_status(self) -> Dict:
        """Get current engine status."""
        snapshot = self._create_portfolio_snapshot()
        return {
            "status": self.status,
            "started_at": self.started_at.isoformat() if self.started_at else None,
            "current_time": datetime.now().isoformat(),
            "portfolio": {
                "total_value": snapshot.total_value,
                "cash": snapshot.cash,
                "position_quantity": snapshot.position_quantity,
                "position_value": snapshot.position_value,
                "pnl_total": snapshot.pnl_total,
                "pnl_daily": snapshot.daily_pnl,
                "return_pct": snapshot.return_pct,
            },
            "trading": {
                "num_trades": len(self.trades),
                "daily_trades": len(self.daily_trades),
                "win_rate": self._calculate_win_rate(),
                "position_status": "OPEN" if self.current_position else "FLAT",
            },
            "models": {
                model: {
                    "last_signal": self.last_signals[model].signal_type.value if model in self.last_signals else None,
                    "confidence": self.last_signals[model].confidence if model in self.last_signals else 0.0,
                    "signal_count": len(self.signal_history[model]),
                } for model in self.config.signal_weights.keys()
            },
            "silver": {
                "beta": self.current_beta,
                "feed_status": self.silver_feed.feed_status()
            }
        }
    
    def process_signal(self, model_name: str, signal: ModelSignal) -> Optional[TradeExecution]:
        """
        Process a new signal from a model with thread/concurrency safety.
        
        Uses dynamic regime-conditional weighting to adjust signal confidence:
        - Each model's raw confidence is scaled by its current dynamic weight
        - Weights are higher for models suited to the current regime
        - Weights adapt over time based on each model's rolling Sharpe
        
        Args:
            model_name: Name of the model generating the signal
            signal: ModelSignal with entry price and confidence
            
        Returns:
            TradeExecution if trade was executed, None otherwise
        """
        with self._lock:
            if self.status != "RUNNING":
                return None
            
            # Automatically update price inside engine to match incoming signal (Lever 4)
            if signal.current_price > 0:
                self.update_price(signal.current_price, signal.timestamp)

            # Store signal
            self.last_signals[model_name] = signal
            self.signal_history[model_name].append(signal)
            
            # --- Hard Confidence Gate (Bug 1 Fix) ---
            # The raw confidence from the ensemble MUST pass the threshold before any dynamic
            # weight multipliers are applied, ensuring the Critic's minimum standard is respected.
            if signal.confidence < self.config.min_confidence:
                logger.debug(
                    f"{model_name} raw signal below threshold: "
                    f"{signal.confidence:.2f} < {self.config.min_confidence}"
                )
                return None
    
            # --- Dynamic Weight Adjustment ---
            # Get current dynamic weights based on regime + recent performance
            if self.config.use_dynamic_weights:
                current_signals_map = {
                    name: sig.signal_type.value
                    for name, sig in self.last_signals.items()
                }
                dynamic_weights = self.weight_adjuster.get_weights(
                    regime=signal.regime,
                    current_signals=current_signals_map,
                )
                model_weight = dynamic_weights.get(model_name, 0.10)
                
                # Scale confidence by model's dynamic weight relative to average
                avg_weight = 1.0 / len(dynamic_weights) if dynamic_weights else 0.14
                weight_multiplier = model_weight / avg_weight
                adjusted_confidence = signal.confidence * weight_multiplier
                
                # Session quality scoring: Boost during first 30 mins of NY session (13:00-13:30 GMT)
                now_gmt = datetime.utcnow()
                if now_gmt.hour == 13 and 0 <= now_gmt.minute <= 30:
                    adjusted_confidence *= 1.15
                    
                adjusted_confidence = min(adjusted_confidence, 1.0)  # Cap at 1.0
                
                logger.debug(
                    f"{model_name} | regime={signal.regime} | "
                    f"raw_conf={signal.confidence:.2f} | weight={model_weight:.3f} | "
                    f"adj_conf={adjusted_confidence:.2f}"
                )
            else:
                adjusted_confidence = signal.confidence
            
            # Check risk limits
            if not self._check_risk_limits():
                logger.warning("Risk limits exceeded, skipping trade")
                return None
                
            # --- Correlation Guard ---
            # Block new Gold entries if a Silver hedge leg is actively open
            if self.position_sizes.get("XAGUSD", 0) != 0 and signal.signal_type in (SignalType.LONG, SignalType.SHORT):
                if not self.current_position or self.current_position.status != TradeStatus.OPEN:
                    logger.warning("Correlation Guard: XAGUSD hedge active. Blocking new Gold entry.")
                    return None
            
            # Check if position already open
            if self.current_position and self.current_position.status == TradeStatus.OPEN:
                # If signal is CLOSE or opposite direction, close existing position
                if signal.signal_type == SignalType.CLOSE or \
                   (self.current_position.signal_type == SignalType.LONG and signal.signal_type == SignalType.SHORT) or \
                   (self.current_position.signal_type == SignalType.SHORT and signal.signal_type == SignalType.LONG):
                    
                    # Enforce minimum holding period (Lever 4)
                    bars_held = getattr(self.current_position, "bars_held", 0)
                    min_hold = getattr(self.config, "min_holding_bars", 8)
                    if bars_held < min_hold:
                        logger.info(
                            f"Hold period block: {bars_held} bars < {min_hold} min holding bars. "
                            f"Ignoring exit/flip signal for trade {self.current_position.trade_id}."
                        )
                        return None
                        
                    closed_trade = self._close_position(signal.timestamp, signal.current_price)
                    if signal.signal_type == SignalType.CLOSE:
                        return closed_trade
                else:
                    logger.debug("Position already open, skipping new signal")
                    return None
            
            # Execute trade (pass adjusted confidence for position sizing)
            if signal.signal_type in (SignalType.LONG, SignalType.SHORT):
                return self._execute_trade(model_name, signal, adjusted_confidence)
            
            return None
    
    def update_price(self, price: float, timestamp: datetime) -> Dict:
        """
        Update current price, calculate P&L, and manage trailing stops.
        
        Args:
            price: Current price for XAUUSD
            timestamp: Current timestamp
            
        Returns:
            Updated portfolio snapshot
        """
        # --- Kalman Hedge Update ---
        # Strictly use T-1 (previous) prices to avoid look-ahead bias
        prev = self.silver_feed.previous
        if prev.is_valid():
            self.current_beta = self.kalman_hedge.update(x_price=prev.xag, y_price=prev.xau)
            
        # Update position P&L if open
        if self.current_position and self.current_position.status == TradeStatus.OPEN:
            trade = self.current_position
            # Increment bars_held only if the bar's timestamp has advanced (prevent duplicate counts)
            last_ts = getattr(trade, "last_bar_timestamp", None)
            if last_ts != timestamp:
                trade.bars_held = getattr(trade, "bars_held", 0) + 1
                trade.last_bar_timestamp = timestamp
                
            pnl, pnl_pct = self._calculate_pnl(
                trade.signal_type,
                trade.entry_price,
                price,
                trade.quantity
            )
            trade.pnl = pnl
            trade.pnl_pct = pnl_pct
            
            # Volatility-Adjusted Trailing Stop Logic
            hwm = trade.high_water_mark
            if hwm is None:
                hwm = price
                
            if trade.signal_type == SignalType.LONG:
                if price > hwm:
                    hwm = price
            else: # SHORT
                if price < hwm:
                    hwm = price
                    
            trade.high_water_mark = hwm
                    
            # Determine trailing % using RL Execution Agent
            rl_params = get_rl_agent().get_execution_parameters(
                regime=trade.regime, 
                volatility=0.02, # Approx daily volatility 
                confidence=trade.confidence
            )
            
            # ATR-based activation logic (using 1% price as proxy for 1xATR)
            atr_proxy = price * 0.01 
            profit_margin = (hwm - trade.entry_price) if trade.signal_type == SignalType.LONG else (trade.entry_price - hwm)
            
            # Trailing Stop Phases
            if profit_margin >= atr_proxy:
                # Phase 3: Trailing (Profit > 1.0x ATR)
                trailing_pct = rl_params["trailing_stop_pct"]
            elif profit_margin >= (atr_proxy * 0.5):
                # Phase 2: Breakeven Jump (Profit > 0.5x ATR)
                # Move stop to exactly the entry price (breakeven) to protect capital
                trailing_pct = 0.00  
                hwm = trade.entry_price 
            else:
                # Phase 1: Fixed Risk (Profit < 0.5x ATR)
                trailing_pct = 0.02  # Fixed 2% stop before breakeven jump
                hwm = trade.entry_price  # Anchor to entry price so it acts as a fixed stop
            
            if trade.signal_type == SignalType.LONG:
                trade.trailing_stop = hwm * (1 - trailing_pct)
                if price <= trade.trailing_stop:
                    logger.info(f"Trailing stop hit for LONG trade {trade.trade_id} @ {price:.2f}")
                    self._close_position(timestamp, price)
            else: # SHORT
                trade.trailing_stop = hwm * (1 + trailing_pct)
                if price >= trade.trailing_stop:
                    logger.info(f"Trailing stop hit for SHORT trade {trade.trade_id} @ {price:.2f}")
                    self._close_position(timestamp, price)
        
        # Create snapshot
        snapshot = self._create_portfolio_snapshot(price)
        self.portfolio_snapshots.append(snapshot)
        
        return {
            "timestamp": timestamp.isoformat(),
            "price": price,
            "portfolio_value": snapshot.total_value,
            "pnl_total": snapshot.pnl_total,
            "position_pnl": self.current_position.pnl if self.current_position else 0.0,
        }
    
